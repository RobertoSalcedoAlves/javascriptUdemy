// a declaração pode ocorrer de três maneiras
const prod1 = {} 
prod1.nome = 'Celular tri massa' // veja como os atributos são declarados
prod1.preco = 700.01

console.log(prod1)

const prod2 = { // aqui o objeto já é arquitetado quando declarado, com seus respectivos atributos
    nome: 'carro popular', // não esqueça da vírgula para separar os atributos
    preco: 25000.00
}

console.log(prod2)